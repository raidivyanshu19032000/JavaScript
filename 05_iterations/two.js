// while and do-while loop

let index = 0 ;

while(index <= 10 ){
    // console.log(`The value of index is : ${index}`);
    // index += 2;
    index = index + 2;
}   

let myArray = ['Flash', 'Superman', 'Batman']
let arr = 0;
while(arr < myArray.length){
    // console.log(`The value of myArray is : ${myArray[arr]}`);
    arr = arr + 1;
}

let score = 11
do {
    console.log(`Score is : ${score}`);
    score++;
} while (score <= 10);